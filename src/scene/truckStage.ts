/**
 * Геометрия дня торговли: где окно фудтрака, где стоит герой и где очередь.
 *
 * Числа сняты с самой сцены, а не выведены из scene-layout.json: у пропса
 * запечён поворот, и «перёд» кузова по локальным осям не угадывается. Мировые
 * габариты food_truck.glb на его месте:
 *
 *   кузов    x 0.39…2.95   z −7.16…−4.35   (ближняя к камере стенка: z = −4.35)
 *   прилавок x 1.64…2.76   y 0.47…0.52
 *
 * Отсюда раздача — по центру прилавка, x ≈ 2.2, у стенки z ≈ −4.35.
 *
 * У кузова нет пола и внутренностей: «внутри» — та же трава. Герой стоит за
 * прилавком так, чтобы доска резала его по пояс, а торс попадал в проём окна.
 * Глубже он повисает над прилавком, ближе — оказывается на улице рядом с
 * очередью и читается как ещё один клиент.
 *
 * Держим числа здесь, а не в компонентах: и Hero, и Customers считают от одной
 * точки, и разъехаться им нельзя.
 */
import * as THREE from 'three'

/** Центр фудтрака на земле. */
export const TRUCK = new THREE.Vector3(1.8441, 0, -5.8892)

/** Центр раздачи по оси кузова: середина прилавка. */
const SERVE_X = 2.45

/** Ближняя к камере стенка кузова — плоскость окна. */
const FACE_Z = -4.35

/** Герой за прилавком, лицом к очереди. */
export const HERO_SEAT = new THREE.Vector3(SERVE_X, 0, FACE_Z - 0.43)

/**
 * Первый в очереди — у края прилавка, чуть правее героя.
 *
 * Ровно напротив окна он вставать не должен: ось +Z уходит по экрану влево, и
 * стоящий «перед» героем клиент оказывается слева от него, будто мимо проходил.
 */
export const QUEUE_HEAD = new THREE.Vector3(SERVE_X + 0.35, 0, FACE_Z + 0.5)

/**
 * Шаг между клиентами. Метр, а не 0.85: очередь должна дотягиваться до ёлки
 * справа, иначе хвост толпится у самого окна.
 */
export const QUEUE_STEP = 1.0

/**
 * Хвост очереди уходит вдоль +X — вправо по экрану от фудтрака.
 *
 * Раньше он шёл вдоль +Z и на экране заворачивал влево, к дому: очередь
 * упиралась в грядки и читалась как толпа у крыльца.
 */
export const QUEUE_DIR = new THREE.Vector3(1, 0, 0)

/**
 * Откуда клиенты приходят: из глубины сцены, из-за ёлок позади фудтрака — на
 * экране это верхний край кадра.
 *
 * Раньше точка лежала справа на линии очереди, внутри поля зрения: клиенты не
 * приходили, а возникали из воздуха посреди травы.
 */
export const SPAWN = new THREE.Vector3(5.6, 0, -11.0)

/**
 * Поворотная точка на пути к очереди: правее хвоста, на линии очереди.
 *
 * Без неё клиент, идущий к голове очереди, срезал бы прямо сквозь кузов
 * фудтрака: коллизий у клиентов нет.
 */
export const APPROACH = new THREE.Vector3(SERVE_X + 4.4, 0, FACE_Z + 0.5)

/** Место i-го в очереди. */
export function queueSpot(i: number, out = new THREE.Vector3()): THREE.Vector3 {
  return out.set(
    QUEUE_HEAD.x + QUEUE_DIR.x * i * QUEUE_STEP,
    0,
    QUEUE_HEAD.z + QUEUE_DIR.z * i * QUEUE_STEP,
  )
}

/** Куда смотреть, чтобы видеть точку (dx, dz). Модель глядит на −Z. */
export function yawTo(dx: number, dz: number): number {
  return Math.atan2(-dx, -dz)
}
